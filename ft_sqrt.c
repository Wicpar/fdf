#include "fdf.h"

t_float		ft_sqrt_fast(t_float x)
{
	t_sqrt_util	y;

	y.f = (float)x;
	y.ul = (0xBE6EB50CUL - y.ul) >> 1;
	y.f = 0.5f * y.f * (3.0f - x * y.f * y.f);
	return (x * (t_float)y.f);
}
