#include "fred_gl.h"

t_vec3		vec3(t_float x, t_float y, t_float z)
{
	t_vec3	new;

	new.x = x;
	new.y = y;
	new.z = z;
	return (new);
}

t_vec3		vec3_1(t_float x)
{
	return (vec3(x, x, x));
}
