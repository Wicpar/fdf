#include "fred_gl.h"

t_vec3		decode(int color)
{
	return (vec3((t_float)((color & 0xFF0000) >> 16) / 255.,
				(t_float)((color & 0x00FF00) >> 8) / 255.,
				(t_float)(color & 0x0000FF) / 255.));
}
