#include "fred_gl.h"

t_vec4		sub_vec4(t_vec4 a, t_vec4 b)
{
	a.x -= b.x;
	a.y -= b.y;
	a.z -= b.z;
	a.w -= b.w;
	return (a);
}

t_vec4		sub_vec4_1(t_vec4 a, t_float b)
{
	a.x -= b;
	a.y -= b;
	a.z -= b;
	a.w -= b;
	return (a);
}
