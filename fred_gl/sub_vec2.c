#include "fred_gl.h"

t_vec2		sub_vec2(t_vec2 a, t_vec2 b)
{
	a.x -= b.x;
	a.y -= b.y;
	return (a);
}

t_vec2		sub_vec2_1(t_vec2 a, t_float b)
{
	a.x -= b;
	a.y -= b;
	return (a);
}
