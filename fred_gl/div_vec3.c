#include "fred_gl.h"

t_vec3		div_vec3(t_vec3 a, t_vec3 b)
{
	a.x /= b.x;
	a.y /= b.y;
	a.z /= b.z;
	return (a);
}

t_vec3		div_vec3_1(t_vec3 a, t_float b)
{
	a.x /= b;
	a.y /= b;
	a.z /= b;
	return (a);
}
