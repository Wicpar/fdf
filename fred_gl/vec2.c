#include "fred_gl.h"

t_vec2		vec2(t_float x, t_float y)
{
	t_vec2	new;

	new.x = x;
	new.y = y;
	return (new);
}

t_vec2		vec2_1(t_float x)
{
	return (vec2(x, x));
}
