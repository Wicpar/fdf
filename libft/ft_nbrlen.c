#include "libft.h"

size_t	ft_nbrlen(int nbr)
{
	int		cnt;

	if (nbr == 0)
		return (1);
	cnt = nbr < 0;
	while (nbr)
	{
		cnt++;
		nbr /= 10;
	}
	return (cnt);
}
